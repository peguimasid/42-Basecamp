int	get_numbers(int *result, char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			result[j] = str[i] - '0';
			j++;
		}
		i++;
	}
	return (*result);
}
